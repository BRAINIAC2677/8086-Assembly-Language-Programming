DEL SMALL

.STACK 100H

.DATA

CR EQU 0DH
LF EQU 0AH
NEWLINE DB CR, LF, '$'

ARR DW 100 DUP (?)

.CODE

MAIN PROC
    
    ; LOADING DATA
    MOV AX, @DATA
    MOV DS, AX
    
    MOV DX, 01H
    CALL TWOS_COMP
    
    ; EXIT DOS
    MOV AH, 4CH
    INT 21H
    
MAIN ENDP


TWOS_COMP PROC
    ; WILL CALC 2S COMPLEMENT OF DX AND STORE IN DX
    
    PUSH AX
    
    MOV AX, 0FFFFH  ; NOT
    SUB AX, DX
    ADD AX, 01H
    MOV DX, AX
    
    POP AX
    
    RET
TWOS_COMP ENDP

 
 
END MAIN


